    /**
     * 设置背景层透明度
     *
     * @param dimAmount 0~1
     */
    fun setDimAmount(dimAmount: Float): EmjioDialog {
        val window = window
        if (window != null) {
            val lp = window.attributes
            // 设置背景层透明度
            lp.dimAmount = dimAmount
            window.attributes = lp
        }
        return this
    }

    /**
     * 设置Window动画
     *
     * @param style R文件
     */
    fun setWindowAnimations(style: Int): EmjioDialog {
        val window = window
        window?.setWindowAnimations(style)
        return this
    }

    /**
     * 设置Dialog宽度
     *
     * @param proportion 和屏幕的宽度比(10代表10%) 0~100
     */
    fun setEmjioDialogWidth(proportion: Int): EmjioDialog {
        val window = window
        if (window != null) {
            val lp = window.attributes
            lp.width = getScreenW(context) * proportion / 100
            window.attributes = lp
        }
        return this
    }

    /**
     * 设置Dialog高度
     *
     * @param proportion 和屏幕的高度比(10代表10%) 0~100
     */
    fun setEmjioDialogHeight(proportion: Int): EmjioDialog {
        val window = window
        if (window != null) {
            val lp = window.attributes
            lp.height = getScreenH(context) * proportion / 100
            window.attributes = lp
        }
        return this
    }

    /**
     * 获取屏幕宽度
     */
    private fun getScreenW(aty: Context): Int {
        val dm = aty.resources.displayMetrics
        return dm.widthPixels
    }

    /**
     * 获取屏幕的高度
     */
    private fun getScreenH(context: Context): Int {
        val wm = context.getSystemService(Context.WINDOW_SERVICE) as WindowManager
        val outMetrics = DisplayMetrics()
        wm.defaultDisplay?.getMetrics(outMetrics)
        return outMetrics.heightPixels
    }
